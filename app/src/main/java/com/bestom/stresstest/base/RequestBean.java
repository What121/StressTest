package com.bestom.stresstest.base;

public class RequestBean {
    private String id;
    private int type=1;     //type == 0?producttest:stresstest
    private String msg;     //

    public RequestBean(String id, String msg) {
        this.id = id;
        this.msg = msg;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
